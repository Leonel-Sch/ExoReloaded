#include <unistd.h>
#include <stdlib.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_is_negative(int n)
{
	if (n >= 0)
		ft_putchar('P');
	else
		ft_putchar('N');
}

int	main(int argc, char **argv)
{
	if (argc > 2)
		return (0);
	ft_is_negative(atoi(argv[1]));
	return (0);
}